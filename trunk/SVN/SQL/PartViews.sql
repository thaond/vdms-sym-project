CREATE OR REPLACE VIEW VDMS.VIEW_ALL_IMA_FILE (
  PART_CODE,
  ENGLISH_NAME,
  VIETNAM_NAME,
  CATEGORY,
  CREATED_DATE,
  MODEL,
  STATUS,
  DATABASE_CODE
)
AS
  SELECT ima01 AS Part_Code,
    ima02      AS English_Name,
    ta_ima030  AS Vietnam_Name,
    ima06      AS CATEGORY,
    ima901     AS created_date,
    ima131     AS MODEL,
    ta_ima070  AS status,
    'DNF'      AS Database_Code
  FROM ima_file@dnp_dblink
  UNION ALL
  SELECT ima01 AS Part_Code,
    ima02      AS English_Name,
    ta_ima030  AS Vietnam_Name,
    ima06      AS CATEGORY,
    ima901     AS created_date,
    ima131     AS MODEL,
    ta_ima070  AS status,
